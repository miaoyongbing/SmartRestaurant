package com.smart.bean;

public class Store {

	private int material_id;
	private String material_name;
	private int material_num;
	public final int getMaterial_id() {
		return material_id;
	}
	public final void setMaterial_id(int material_id) {
		this.material_id = material_id;
	}
	public final String getMaterial_name() {
		return material_name;
	}
	public final void setMaterial_name(String material_name) {
		this.material_name = material_name;
	}
	public final int getMaterial_num() {
		return material_num;
	}
	public final void setMaterial_num(int material_num) {
		this.material_num = material_num;
	}
	
}
