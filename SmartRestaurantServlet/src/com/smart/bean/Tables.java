package com.smart.bean;

public class Tables {

	private int table_id;
	private int table_num;
	private int table_type;
	public final int getTable_id() {
		return table_id;
	}
	public final void setTable_id(int table_id) {
		this.table_id = table_id;
	}
	public final int getTable_num() {
		return table_num;
	}
	public final void setTable_num(int table_num) {
		this.table_num = table_num;
	}
	public final int getTable_type() {
		return table_type;
	}
	public final void setTable_type(int table_type) {
		this.table_type = table_type;
	}
	
}
