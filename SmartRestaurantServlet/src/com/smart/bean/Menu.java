package com.smart.bean;

public class Menu {
	private int food_id;
	private int food_price;
	private String food_picture;
	private int is_available;
	private String food_name;
	public final int getFood_id() {
		return food_id;
	}
	public final void setFood_id(int food_id) {
		this.food_id = food_id;
	}
	public final int getFood_price() {
		return food_price;
	}
	public final void setFood_price(int food_price) {
		this.food_price = food_price;
	}
	public final String getFood_picture() {
		return food_picture;
	}
	public final void setFood_picture(String food_picture) {
		this.food_picture = food_picture;
	}
	public final int getIs_available() {
		return is_available;
	}
	public final void setIs_available(int is_available) {
		this.is_available = is_available;
	}
	public final String getFood_name() {
		return food_name;
	}
	public final void setFood_name(String food_name) {
		this.food_name = food_name;
	}
	
}
